package School;

public class StudentAdress {
    public String area;
    public  String city;
    public  String pinCode;


    @Override
    public String toString() {
        return "StudentAdress{" +
                "area='" + area + '\'' +
                ", city='" + city + '\'' +
                ", pinCode='" + pinCode + '\'' +
                '}';
    }
}
